package com.bean;



public class Shopcur{
	
	private int shopcurId;
	private int number;
	private int sum;
	private int userId;
	private int bookId;
	
	
	public int getShopcurId() {
		return shopcurId;
	}
	public void setShopcurId(int shopcurId) {
		this.shopcurId = shopcurId;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public int getBookId() {
		return bookId;
	}
	public void setBookId(int bookId) {
		this.bookId = bookId;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	
	
	
}
